#include<bits/stdc++.h>
using namespace std;
const int qwe=5e2+5;
int n,T;
int mp[qwe][qwe];
int main(){
    scanf("%d", &T);
    while (T--)
    {
        scanf("%d", &n);
        for (int i = 1; i <= n; i++){
            for (int j = 1; j <= n; j++){
                scanf("%d" ,&mp[i][j]);
            }
        }
        
    }
    // scanf("%d",&n);
}